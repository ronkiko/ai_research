"""Режимы обучения адаптера ИИ и справка по ним.

Режим = как train() превращает исход хода в изменение весов. Справка (help) —
бытовое объяснение для оператора, без математики; движок отдаёт её в UI для
окна выбора/листания режимов.
"""
from __future__ import annotations

from modules.base import TrainModeInfo

# Ключи режимов.
SUPERVISED = "supervised"
RL = "rl"

# Справка по каждому режиму: бытовым языком, как объяснение настольной игры.
SUPERVISED_HELP = (
    "Модель учится «с ответом». Каждый ход мир не только платит фишки за угадал "
    "или забирает за мимо, но и показывает, что реально выпало — правильный "
    "ответ. Модель тянет свои числа к тому, чтобы её предсказание совпадало с "
    "этим ответом, а действие — это просто «выбираю то, что предсказал». "
    "Учится быстро и устойчиво, но требует, чтобы мир честно раскрывал правильный "
    "ответ. На наших играх (мяч, кормушка) «предсказать мир» и «выиграть» — одно и "
    "то же, поэтому модель быстро учится угадывать."
)

RL_HELP = (
    "Модель учится «по подкреплению», без ответа. Мир не показывает, что выпало "
    "на самом деле, — только даёт фишку за угадал или забирает за мимо. Модель "
    "сама пробует разные ходы (случайно, не всегда выбирая то, что кажется лучшим) "
    "и со временем повышает шанс действий, что приносили фишки, и снижает шанс "
    "невыгодных. Действие выбирается случайно, с вероятностью, зависящей от "
    "чисел модели. Учится медленнее и шумнее, зато работает даже когда мир вообще "
    "не раскрывает правильный ответ. На наших играх приходит к тому же "
    "результату, что и supervised, но ведёт себя как живой ученик: тыкается и "
    "корректируется, не подглядывая в ответ. Разница со supervised видна на "
    "играх, где «угадать мир» не равно «выиграть» (например, нужно сделать "
    "наоборот дилеру) — там режимы приводят модель к разным стратегиям."
)

TRAIN_MODES: list[TrainModeInfo] = [
    TrainModeInfo(
        key=SUPERVISED,
        title="Supervised — с ответом",
        summary="учится по правильному ответу, который раскрывает мир",
        help=SUPERVISED_HELP,
    ),
    TrainModeInfo(
        key=RL,
        title="RL — по подкреплению",
        summary="учится по фишкам, пробуя ходы вслепую, без ответа",
        help=RL_HELP,
    ),
]


def mode_info(key: str) -> TrainModeInfo | None:
    for m in TRAIN_MODES:
        if m.key == key:
            return m
    return None


def is_valid(key: str) -> bool:
    return any(m.key == key for m in TRAIN_MODES)