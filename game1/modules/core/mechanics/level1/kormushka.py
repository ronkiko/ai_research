"""Стол «Кормушка» — среда с состоянием: 2 состояния, 2 параметра.

Корм появляется в левой/правой кормушке, мир любит повторять сторону с
вероятностью Q_REPEAT. Агент видит прошлую кормушку и решает, куда бежать.
Здесь у сети появляется вход (прошлая сторона) — значит, есть и вес, и
смещение: два параметра.
"""
from __future__ import annotations

import random

from modules.base import SelectResult, Status
from ..base import Mechanics, Observation, Outcome


class KormushkaMechanics(Mechanics):
    KEY = "kormushka"
    TITLE = "Кормушка"
    SUMMARY = "две кормушки, беги туда, где, по-твоему, будет корм (2 параметра)"

    RULES = (
        "Две кормушки — левая и правая. Каждый ход корм появляется в одной из "
        "них. Ты видишь, где корм был в прошлый раз, и решаешь, к какой бежать. "
        "Угадал — сыт (фишка), нет — голоден. Мир любит повторяться: если корм "
        "был слева, скорее всего и сейчас будет слева. Но не всегда — иногда "
        "мир обманывает и прыгает на другую сторону. Хитрость в том, чтобы "
        "подметить эту привычку и ставить туда же, где было в прошлый раз.\n\n"
        "Альтернативная постановка:\n"
        "  Вопрос: в какой кормушке будет корм?\n"
        "  Правда: выбранная кормушка совпала с той, где оказался корм.\n"
        "  Подсказка: видно, где корм был в прошлый раз."
    )
    LEARNS = (
        "У модели два числа. Одно — общая привычка бежать направо, независимо "
        "ни от чего. Второе — насколько склоняться туда же, где корм был "
        "в прошлый раз. Глядя на прошлую кормушку, модель учится: «было слева "
        "— чаще ставь слева», и подкручивает второе число. Первое помогает, "
        "когда мир только начал игру и прошлого ещё нет."
    )

    # Домовое правило стола: мир повторяет прошлую сторону с такой вероятностью.
    Q_REPEAT = 0.7

    def __init__(self, seed: int | None = None) -> None:
        self._rng = random.Random(seed)
        self._prev: int | None = None

    def sit(self) -> SelectResult:
        self._prev = self._rng.randint(0, 1)
        return SelectResult(Status.OK, "кормушки готовы, корм положен", self.info())

    def observe(self) -> Observation:
        # Агент видит, где корм был в прошлый раз.
        return Observation(state=(self._prev,))

    def step(self, action: int) -> Outcome:
        repeat = self._rng.random() < self.Q_REPEAT
        revealed = self._prev if repeat else 1 - self._prev
        reward = 1 if action == revealed else -1
        self._prev = revealed
        return Outcome(revealed=revealed, reward=reward, target=revealed, action=action)

    def world_lore(self) -> list[str]:
        # Литературно: мир упрям и любит повторяться, но иногда обманывает и
        # прыгает на другую сторону — без формул и без числа 0.7.
        return [
            "Мир упрям.",
            "",
            "Куда корм положили в прошлый раз, "
            "туда он скорее всего положит снова "
            "— мир любит повторяться. Но иногда "
            "корм прыгает на другую сторону.",
        ]